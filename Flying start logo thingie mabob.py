import turtle, time
screen = turtle.Screen()
screen.setup(500,500)
screen.bgcolor("#890E35")
# Create the turtle to be used
t = turtle.Turtle()
t.fillcolor('white')
t.speed(0)
# Move to the start position
t.setheading(270)
t.penup()
t.forward(75)
t.setheading(0)
t.pendown()
# Draw the first C
t.begin_fill()
t.circle(100, -180)
t.setheading(270)
t.forward(40)
t.setheading(180)
t.circle(60, 180)
t.setheading(270)
t.forward(40)
t.end_fill()
# Move to second c start position
t.penup()
t.forward(50)
t.pendown()
t.setheading(0)
# Draw second c
t.begin_fill()
t.circle(150,-180)
t.setheading(270)
t.forward(40)
t.setheading(180)
t.circle(110,180)
t.setheading(270)
t.forward(40)
t.end_fill()
# Move to last c start position
t.penup()
t.setheading(180)
t.forward(160)
t.setheading(90)
t.forward(150)
t.pendown()
# Draw final c
t.begin_fill()
t.setheading(270)
t.circle(160,180)
t.setheading(360)
t.forward(40)
t.setheading(90)
t.circle(200,-180)
t.setheading(360)
t.forward(40)
t.end_fill()
# Wait
time.sleep(3)
# End
turtle.bye()
